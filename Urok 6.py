# Словарь с клиентами и номерами комнат
клиенты = {}

# Функция добавления клиента
def добавить_клиента():
    # Вывести список занятых номеров
    print("Занятые номера:")
    for имя, номер in клиенты.items():
        print(f"- {номер}: {имя}")

    # Ввести имя клиента и номер комнаты
    имя = input("Введите имя клиента: ")
    номер = input("Введите номер комнаты: ")

    # Добавить клиента в словарь
    клиенты[имя] = номер
    print(f"Клиент {имя} добавлен в номер {номер}")

# Функция удаления клиента
def удалить_клиента():
    # Ввести имя клиента для удаления
    имя = input("Введите имя клиента для удаления: ")

    # Удалить клиента из словаря
    if имя in клиенты:
        del клиенты[имя]
        print(f"Клиент {имя} удален")
    else:
        print("Клиент не найден")

# Функция отображения занятых номеров
def показать_занятые_номера():
    # Вывести список занятых номеров
    print("Занятые номера:")
    for номер, имя in клиенты.items():
        print(f"- {номер}: {имя}")

# Цикл для взаимодействия с пользователем
while True:
    # Вывести меню
    print("Выберите действие:")
    print("1. Добавить клиента")
    print("2. Удалить клиента")
    print("3. Показать занятые номера")
    print("4. Выход")

    # Ввести номер действия
    выбор = input("Введите номер действия: ")

    # Выполнить действие в зависимости от выбора пользователя
    if выбор == "1":
        добавить_клиента()
    elif выбор == "2":
        удалить_клиента()
    elif выбор == "3":
        показать_занятые_номера()
    elif выбор == "4":
        break
    else:
        print("Неверный выбор")